// Aerie · 云栖 — Phase 7: build-icon-sprite.js
// Aggregates the 9 single SVG icon files in assets/icons/ into a single
// sprite.svg with <symbol> elements. Output is loaded into the renderer
// via inline injection in index.html (zero extra network request).
//
// Style base: 24x24 viewBox, stroke-width 2, currentColor, round caps.
// Naming: {category}-{name}-{size}.svg → icon-{category}-{name}

const fs = require("fs");
const path = require("path");

const ICONS_DIR = path.join(__dirname, "..", "src", "renderer", "assets", "icons");
const SPRITE_OUT = path.join(ICONS_DIR, "sprite.svg");

// Ordered list of source files (must exist in icons dir)
const ICONS = [
  "ui_close_24.svg",
  "ui_attach_24.svg",
  "ui_copy_24.svg",
  "ui_warning_24.svg",
  "mood_neutral_24.svg",
  "mood_joy_24.svg",
  "mood_sad_24.svg",
  "mood_anger_24.svg",
  "mood_fear_24.svg",
];

function extractInnerSvg(svgSource) {
  // Strip XML comments and outer <svg ...> wrapper, keep inner markup.
  // The inner markup is wrapped in <symbol id="..."> by this script.
  const noComments = svgSource.replace(/<!--[\s\S]*?-->/g, "");
  // Find the <svg ...> opening tag
  const openTag = noComments.match(/<svg\b[^>]*>/);
  if (!openTag) {
    throw new Error("Could not find <svg> root tag in icon source");
  }
  const startIdx = openTag.index + openTag[0].length;
  // Find the matching </svg> close tag (assume single root svg here)
  const closeIdx = noComments.lastIndexOf("</svg>");
  if (closeIdx === -1 || closeIdx < startIdx) {
    throw new Error("Could not find </svg> close tag");
  }
  return noComments.slice(startIdx, closeIdx).trim();
}

function filenameToSymbolId(filename) {
  // ui_close_24.svg → icon-ui-close
  const base = filename.replace(/\.svg$/, "");
  const parts = base.split("_");
  if (parts.length < 3) {
    throw new Error(`Bad icon filename ${filename} (expected category-name-size.svg)`);
  }
  const [category, ...rest] = parts;
  const name = rest.slice(0, -1).join("_");
  return `icon-${category}-${name}`;
}

function build() {
  const symbols = [];
  for (const filename of ICONS) {
    const filePath = path.join(ICONS_DIR, filename);
    if (!fs.existsSync(filePath)) {
      console.warn(`[build-icon-sprite] Missing: ${filename}`);
      continue;
    }
    const source = fs.readFileSync(filePath, "utf-8");
    const inner = extractInnerSvg(source);
    const symbolId = filenameToSymbolId(filename);
    // Wrap inner in a <symbol> with our normalized attributes
    const symbol = `<symbol id="${symbolId}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">\n${inner}\n</symbol>`;
    symbols.push({ id: symbolId, body: symbol, filename });
  }

  const header = `<!--\n  Aerie · 云栖 — Icon Sprite (Phase 7)\n  Generated by build-icon-sprite.js\n  Source: assets/icons/*.svg\n  Style: 24x24 viewBox, stroke-width 2, currentColor, round caps.\n  Generated: ${new Date().toISOString()}\n-->`;
  const sprite = `<svg xmlns="http://www.w3.org/2000/svg" style="position:absolute; width:0; height:0; overflow:hidden;" aria-hidden="true">\n  <defs>\n${symbols.map((s) => `    ${s.body}`).join("\n")}\n  </defs>\n</svg>\n`;

  fs.writeFileSync(SPRITE_OUT, header + "\n" + sprite, "utf-8");
  console.log(`[build-icon-sprite] Wrote ${symbols.length} symbols to ${path.relative(process.cwd(), SPRITE_OUT)}`);
  symbols.forEach((s) => {
    console.log(`  • ${s.id}  ←  ${s.filename}`);
  });
}

if (require.main === module) {
  build();
}

module.exports = { build, extractInnerSvg, filenameToSymbolId };
